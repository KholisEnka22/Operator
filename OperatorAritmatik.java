package com.dicoding.javafundamental.operator;

public class OperatorAritmatik {
    public static void main(String[]args) {
        System.out.println("Operator Penjumlahan");
        int penjumlahan = 1 + 2;
        System.out.println("Hasil Penjumlahan Dari 1 + 2 = " + penjumlahan);
        System.out.println();
        
        System.out.println("Operator Pengurangan");
        int pengurangan = 2 - 1;
        System.out.println("Hasil Pengurangan Dari 2 - 1 = " + pengurangan);
        System.out.println();
        
        System.out.println("Operator Perkalian");
        int perkalian = 1 * 2;
        System.out.println("Hasil Perkalian Dari 1 * 2 = " + perkalian);
        System.out.println();
        
        System.out.println("Operator Pembagian");
        int pembagian = 4 / 2;
        System.out.println("Hasil Pembagian Dari 4 / 2 = " + pembagian);
        System.out.println();
        
        System.out.println("Operator Habis Bagi");
        int habisSisa = 4 % 2;
        System.out.println("Hasil Habis Bagi Dari 4 % 2 = " + habisSisa);
        System.out.println();
        int habisSisaLain = 5 % 2;
        System.out.println("Hasil Habis Sisa Lain Dari 5 % 2 = " + habisSisaLain);
        System.out.println();
    }
}
